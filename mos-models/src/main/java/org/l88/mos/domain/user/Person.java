package org.l88.mos.domain.user;

public class Person {
	/** 个人ID */
	private Long id;
	/** 姓名 */
	private java.lang.String name;
	/** 员工ID */
	private Long employeeId;
	/** 性别 */
	private java.lang.String gender;
	/** 生日 */
	private java.lang.String birthDate;
	/** 手机号 */
	private java.lang.String mobileNo;
	/** 国家代码 */
	private java.lang.String countryAlphaId;
	/** 省份代码 */
	private java.lang.String stateId;
	/** 城市代码 */
	private java.lang.String cityId;
	/** 家庭地址 */
	private java.lang.String homeAddress;
	/** 家庭电话 */
	private java.lang.String homeTelphone;
	/** 家庭邮编 */
	private java.lang.String homePostCode;
	/** 电子邮件 */
	private java.lang.String email;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public java.lang.String getName() {
		return name;
	}

	public void setName(java.lang.String name) {
		this.name = name;
	}

	public Long getEmployeeId() {
		return employeeId;
	}

	public void setEmployeeId(Long employeeId) {
		this.employeeId = employeeId;
	}

	public java.lang.String getGender() {
		return gender;
	}

	public void setGender(java.lang.String gender) {
		this.gender = gender;
	}

	public java.lang.String getBirthDate() {
		return birthDate;
	}

	public void setBirthDate(java.lang.String birthDate) {
		this.birthDate = birthDate;
	}

	public java.lang.String getMobileNo() {
		return mobileNo;
	}

	public void setMobileNo(java.lang.String mobileNo) {
		this.mobileNo = mobileNo;
	}

	public java.lang.String getCountryAlphaId() {
		return countryAlphaId;
	}

	public void setCountryAlphaId(java.lang.String countryAlphaId) {
		this.countryAlphaId = countryAlphaId;
	}

	public java.lang.String getStateId() {
		return stateId;
	}

	public void setStateId(java.lang.String stateId) {
		this.stateId = stateId;
	}

	public java.lang.String getCityId() {
		return cityId;
	}

	public void setCityId(java.lang.String cityId) {
		this.cityId = cityId;
	}

	public java.lang.String getHomeAddress() {
		return homeAddress;
	}

	public void setHomeAddress(java.lang.String homeAddress) {
		this.homeAddress = homeAddress;
	}

	public java.lang.String getHomeTelphone() {
		return homeTelphone;
	}

	public void setHomeTelphone(java.lang.String homeTelphone) {
		this.homeTelphone = homeTelphone;
	}

	public java.lang.String getHomePostCode() {
		return homePostCode;
	}

	public void setHomePostCode(java.lang.String homePostCode) {
		this.homePostCode = homePostCode;
	}

	public java.lang.String getEmail() {
		return email;
	}

	public void setEmail(java.lang.String email) {
		this.email = email;
	}

}
